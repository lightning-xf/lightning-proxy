  String get(String key, {Map<String, dynamic>? args}) {
    // 优先从当前语言包找
    String? value = _localizedValues[locale.languageCode]?[key];

    // 如果当前是英文且没找到，尝试从中文包找（作为回退）
    if (value == null && locale.languageCode != 'zh') {
      value = _localizedValues['zh']?[key];
    }

    // 如果还是没找到，直接显示 key（通常是英文标识符）
    value ??= key;

    if (args != null) {
      args.forEach((key, val) {
        value = value!.replaceAll('{$key}', val.toString());
      });
    }
    return value!;
  }

  static S of(BuildContext context, [WidgetRef? ref]) {
    // Force reactive update if ref is provided
    if (ref != null) {
      final locale = ref.watch(localizationProvider);
      return S(locale);
    }
    return Localizations.of<S>(context, S) ?? S(const Locale('zh', 'CN'));
  }
}

class _SDelegate extends LocalizationsDelegate<S> {
  const _SDelegate();

  @override
  bool isSupported(Locale locale) => ['en', 'zh'].contains(locale.languageCode);

  @override
  Future<S> load(Locale locale) => SynchronousFuture<S>(S(locale));

  @override
  bool shouldReload(_SDelegate old) => false;
}
