class LocaleNotifier extends StateNotifier<Locale> {
  LocaleNotifier(String? langCode, String? countryCode)
      : super(_initLocale(langCode, countryCode));

  static Locale _initLocale(String? langCode, String? countryCode) {
    if (langCode != null) {
      return Locale(langCode, countryCode);
    }
    // 探测系统语言：如果是中文环境则默认为 zh_CN，否则默认为 en_US
    final systemLocale = PlatformDispatcher.instance.locale;
    if (systemLocale.languageCode.contains('zh')) {
      return const Locale('zh', 'CN');
    }
    return const Locale('en', 'US');
  }

  Future<void> setLocale(Locale locale) async {
    state = locale;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString('language_code', locale.languageCode);
    await prefs.setString('country_code', locale.countryCode ?? '');
  }
}

final localizationProvider =
    StateNotifierProvider<LocaleNotifier, Locale>((ref) {
  // This will be overridden in main.dart
  return LocaleNotifier(null, null);
});

